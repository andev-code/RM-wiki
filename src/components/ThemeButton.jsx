import { useState, useEffect } from "react";
import { useTheme } from "../hooks/useTheme";

export function ThemeButton() {
  const { isDark, setIsDark } = useTheme();

  const handleClick = () => {
    const newTheme = !isDark;
    setIsDark(newTheme);
    window.localStorage.setItem("theme", JSON.stringify(newTheme));
  };

  return (
    <div
      className="w-full h-fit px-5 flex justify-center items-center bg-cyan-600 bg-opacity-10 rounded-xl hover:bg-opacity-30 dark:hover:bg-opacity-20 cursor-pointer transition-all"
      onClick={handleClick}
    >
      <p className="text-gray-800 dark:text-gray-200 text-md font-semibold">
        Theme
      </p>
      <button className="w-8 h-8 flex justify-center items-center transition-all">
        {isDark ? (
          <svg
            stroke="currentColor"
            fill="white"
            stroke-width="0"
            viewBox="0 0 16 16"
            height="1em"
            width="1em"
            xmlns="http://www.w3.org/2000/svg"
          >
            <path d="M8 12a4 4 0 1 0 0-8 4 4 0 0 0 0 8zM8 0a.5.5 0 0 1 .5.5v2a.5.5 0 0 1-1 0v-2A.5.5 0 0 1 8 0zm0 13a.5.5 0 0 1 .5.5v2a.5.5 0 0 1-1 0v-2A.5.5 0 0 1 8 13zm8-5a.5.5 0 0 1-.5.5h-2a.5.5 0 0 1 0-1h2a.5.5 0 0 1 .5.5zM3 8a.5.5 0 0 1-.5.5h-2a.5.5 0 0 1 0-1h2A.5.5 0 0 1 3 8zm10.657-5.657a.5.5 0 0 1 0 .707l-1.414 1.415a.5.5 0 1 1-.707-.708l1.414-1.414a.5.5 0 0 1 .707 0zm-9.193 9.193a.5.5 0 0 1 0 .707L3.05 13.657a.5.5 0 0 1-.707-.707l1.414-1.414a.5.5 0 0 1 .707 0zm9.193 2.121a.5.5 0 0 1-.707 0l-1.414-1.414a.5.5 0 0 1 .707-.707l1.414 1.414a.5.5 0 0 1 0 .707zM4.464 4.465a.5.5 0 0 1-.707 0L2.343 3.05a.5.5 0 1 1 .707-.707l1.414 1.414a.5.5 0 0 1 0 .708z"></path>
          </svg>
        ) : (
          <svg
            stroke="currentColor"
            fill="#0ac"
            stroke-width="0"
            viewBox="0 0 24 24"
            height="1em"
            width="1em"
            xmlns="http://www.w3.org/2000/svg"
          >
            <g>
              <path fill="none" d="M0 0h24v24H0z"></path>
              <path d="M11.38 2.019a7.5 7.5 0 1 0 10.6 10.6C21.662 17.854 17.316 22 12.001 22 6.477 22 2 17.523 2 12c0-5.315 4.146-9.661 9.38-9.981z"></path>
            </g>
          </svg>
        )}
      </button>
    </div>
  );
}
